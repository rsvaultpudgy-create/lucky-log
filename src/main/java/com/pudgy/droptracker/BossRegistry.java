	public static final class Drop
	{
		public final String name;
		public final double oneInX;
		public final boolean pet;
		public final boolean notable;
		public final String note;
		public Drop(String name, double oneInX, boolean pet, boolean notable, String note)
		{
			this.name = name; this.oneInX = oneInX; this.pet = pet; this.notable = notable; this.note = note;
		}
		public Drop(String name, double oneInX)
		{
			this(name, oneInX, false, true, null);
		}
		public Drop(String name, double oneInX, boolean pet, String note)
		{
			this(name, oneInX, pet, true, note);
		}
		public static Drop common(String name, double oneInX)
		{
			return new Drop(name, oneInX, false, false, null);
		}
	}

	public static final class Boss
	{
		public final String display;
		public final List<Drop> drops;
		public Boss(String display, Drop... drops)
		{
			this.display = display;
			this.drops = Arrays.asList(drops);
		}

		public Double rateFor(String itemName)
		{
			for (Drop d : drops)
			{
				if (d.name.equalsIgnoreCase(itemName))
				{
					return d.oneInX;
				}
			}
			return null;
		}
		public List<Drop> notableDrops()
		{
			List<Drop> r = new ArrayList<>();
			for (Drop d : drops)
			{
				if (d.notable)
				{
					r.add(d);
				}
			}
			return r;
		}
	}

	private static final Map<String, Boss> BY_NAME = new LinkedHashMap<>();
	// Some sources fire LootReceived under a different name than the boss display (the killed NPC,
	// the reward chest, etc.). Map those alternate loot names onto the registry display name.
	private static final Map<String, String> ALIASES = new LinkedHashMap<>();
	private static void add(Boss b)
	{
		BY_NAME.put(b.display.toLowerCase(), b);
	}
	private static void alias(String lootName, String display)
	{
		ALIASES.put(lootName.toLowerCase(), display.toLowerCase());
	}
	public static Boss byLootName(String name)
	{
		if (name == null)
		{
			return null;
		}
		String key = name.toLowerCase();
		Boss b = BY_NAME.get(key);
		if (b == null)
		{
			String a = ALIASES.get(key);
			if (a != null)
			{
				b = BY_NAME.get(a);
			}
		}
		return b;
	}
	public static List<Boss> all()
	{
		List<Boss> l = new ArrayList<>(BY_NAME.values());
		l.sort((a, b) -> a.display.compareToIgnoreCase(b.display));
		return l;
	}

